document.getElementById('contact-form').addEventListener('submit', function(event) {
  event.preventDefault(); // Prevent the form from submitting normally

  // Get form values
  const name = document.getElementById('name').value;
  const email = document.getElementById('email').value;
  const phone = document.getElementById('phone').value;

  // You can replace this with your desired method of processing the form data
  // For example, you could send the data to a server using AJAX or save it to a database
  console.log(`Name: ${name}\nEmail: ${email}\nPhone: ${phone}`);
  
  // Display a thank you message
  const mainContent = document.querySelector('main');
  mainContent.innerHTML = '<h2>Obrigado por entrar em contato!</h2>';
});
